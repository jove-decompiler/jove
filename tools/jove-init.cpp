int InitTool::Run(void) {
  if (!fs::exists(opts.Prog)) {
    WithColor::error() << llvm::formatv("failed to open {0}\n", opts.Prog);
    return 1;
  }

  IgnoreCtrlC();

  const bool firmadyne = fs::exists("/firmadyne/libnvram.so"); /* XXX */

  //
  // run program with LD_TRACE_LOADED_OBJECTS=1 and no arguments. capture the
  // standard output, which will tell us what binaries are needed by prog.
  //
  std::string dynlink_stdout;
  {
    std::string path_to_stdout = temporary_dir() + "/trace_loaded_objects.stdout.txt";
    std::string path_to_stderr = temporary_dir() + "/trace_loaded_objects.stderr.txt";

    int rc = RunExecutableToExit(
        opts.Prog.c_str(),
        [&](auto Arg) {
          Arg(opts.Prog);
        },
        [&](auto Env) {
          InitWithEnviron(Env);

          Env("LD_TRACE_LOADED_OBJECTS=1");

          if (firmadyne) /* XXX */
            Env("LD_PRELOAD=/firmadyne/libnvram.so");
        },
        path_to_stdout,
        path_to_stderr);

    if (rc) {
      WithColor::error() << llvm::formatv(
          "LD_TRACE_LOADED_OBJECTS=1 {0} returned nonzero exit code {1}",
          opts.Prog, rc);
      return 1;
    }

    dynlink_stdout = read_file_into_string(path_to_stdout.c_str());
  }

  //
  // parse the standard output from the dynamic linker to produce a set of paths
  // to binaries that will be added to the jv
  //
  std::vector<std::string> binary_paths;
  binary_paths.reserve(3);

  //
  // executable should be first
  //
  binary_paths.push_back(fs::canonical(opts.Prog).string());

  auto vdso_pair = GetVDSO();
  bool HasVDSO = vdso_pair.first != nullptr;

  std::vector<uint8_t> vdso;
  if (HasVDSO) {
    vdso.resize(vdso_pair.second);
    memcpy(&vdso[0], vdso_pair.first, vdso.size());
  }

  assert(vdso.size() % 4096 == 0);

  if (!HasVDSO) {
#if defined(TARGET_MIPS32)
    vdso.resize(sizeof(a_vdso));
    memcpy(&vdso[0], &a_vdso[0], vdso.size());
#else
    vdso.resize(4096);
    memset(&vdso[0], 0, vdso.size());
#endif
  }

  {
    char path[0x100];
    snprintf(path, sizeof(path), "%s/linux-vdso.so", temporary_dir().c_str());

    int fd = ::open(path, O_CREAT | O_TRUNC | O_WRONLY, 0666);
    ssize_t ret = robust_write(fd, &vdso[0], vdso.size());
    if (ret < 0) {
      WithColor::error() << llvm::formatv(
          "failed to write vdso to temporary path {0} ({1})\n", path,
          strerror(-ret));
      return 1;
    }

    ::close(fd);

    binary_paths.push_back(path);
  }

  std::string::size_type pos = 0;
  for (;;) {
    std::string::size_type arrow_pos = std::min(dynlink_stdout.find("\t/", pos),
                                                dynlink_stdout.find(" /", pos));

    if (arrow_pos == std::string::npos)
      break;

    pos = arrow_pos + strlen(" /") - 1;

    //
    // XXX XXX XXX we do not support DSO's whose path contains a space
    //
    std::string::size_type space_pos = dynlink_stdout.find(' ', pos);

    if (space_pos == std::string::npos)
      break;

    std::string path = dynlink_stdout.substr(pos, space_pos - pos);
    if (!fs::exists(path)) {
      WithColor::warning() << "path from dynamic linker '" << path << "' is bogus\n";
      continue;
    }

    std::string bin_path = fs::canonical(path).string();
    if (IsVerbose())
      llvm::errs() << llvm::formatv("path = {0} bin_path = {1}\n", path, bin_path);

    if (std::find(binary_paths.begin(), binary_paths.end(), bin_path) != binary_paths.end())
      continue; /* duplicate */

    binary_paths.push_back(bin_path);
  }

  //
  // get the path to the dynamic linker (i.e. program interpreter)
  //
  fs::path rtld_path = fs::canonical(program_interpreter_of_executable(opts.Prog.c_str()));

  if (IsVerbose())
    WithColor::note() << llvm::formatv("rtld_path={0}\n", rtld_path.c_str());

  {
    unsigned Idx;
    for (Idx = 0; Idx < binary_paths.size(); ++Idx) {
      if (fs::equivalent(binary_paths[Idx], rtld_path)) {
        if (IsVerbose())
          WithColor::note() << llvm::formatv("found rtld! idx={0} path={1} rtld_path={2}\n", Idx, binary_paths[Idx], rtld_path.c_str());
        goto Found;
      }
    }

    /* if we got here, we failed to find the dynamic linker */
    WithColor::error() << llvm::formatv("couldn't find rtld! {0}\n", rtld_path.c_str());
    return 1;

Found:

    if (IsVerbose())
      WithColor::error() << llvm::formatv("rtld in binary_paths: idx={0}\n", Idx);

    if (Idx != 1) {
      //
      // modify binary_paths so that the dynamic linker is at index 1.
      //
      {
        auto it = binary_paths.begin();
        std::advance(it, Idx);
        binary_paths.erase(it);
      }

      {
        auto it = std::next(binary_paths.begin());
        binary_paths.insert(it, rtld_path.string()); /* just before vdso */
      }
    }
  }

  //
  // point of no return
  //
  {
    ip_scoped_lock<ip_mutex> lck(jv.binaries_mtx);

    jv.clear();
  }

  tiny_code_generator_t tcg;
  disas_t disas;
  explorer_t E(jv, disas, tcg, opts.VeryVerbose);

  //
  // process the binaries
  //
  std::for_each(binary_paths.begin(), binary_paths.end(),
                [&](const std::string &path) {
                  if (IsVerbose())
                    llvm::errs() << std::string("adding \"" + path + "\"\n");

                  jv.Add(path.c_str(), E);
                });

  { ip_scoped_lock<ip_mutex> lck(jv.binaries_mtx); }

  /* FIXME */
  for_each_binary(jv, [&](binary_t &b) {
                  if (b.path_str() == binary_paths.at(0)) {
                  b.IsExecutable = true;
                  llvm::errs() << "exe is " << index_of_binary(b, jv) << " (" << b.path_str() << ")\n";
                  }
                  });

  /* FIXME */
  for_each_binary(jv, [&](binary_t &b) {
                  if (b.path_str() == rtld_path.string()) {
                  b.IsDynamicLinker = true;
                  llvm::errs() << "dynl is " << index_of_binary(b, jv) << " (" << b.path_str() << ")\n";
                  }
                  });

  /* FIXME */
  for_each_binary(jv, [&](binary_t &b) {
                  if (b.path_str() == binary_paths.at(2)) {
                  b.IsVDSO = true;
                  llvm::errs() << "vdso is " << index_of_binary(b, jv) << " (" << b.path_str() << ")\n";
                  }
                  });

  //
  // firmadyne janky hack
  //
  if (firmadyne) {
    for (binary_t &b : jv.Binaries) {
      if (b.path_str().find("firmadyne") != std::string::npos) {
        b.IsDynamicallyLoaded = true;
        goto found;
      }
    }

    assert(false && "firmadyne DSO not found?");

found:
    ;
  }

  return 0;
}

std::string program_interpreter_of_executable(const char *exepath) {
  std::string res;

  auto BinPair = CreateBinaryFromFile(exepath);

  obj::Binary *B = BinPair.getBinary();
  if (!llvm::isa<ELFO>(B)) {
    WithColor::error() << llvm::formatv("{0}: invalid binary\n", __func__);
    return res;
  }

  const ELFO &O = *llvm::cast<ELFO>(B);
  const ELFF &E = *O.getELFFile();

  auto ExpectedHeaders = E.program_headers();
  if (ExpectedHeaders) {
    for (const Elf_Phdr &Phdr : *ExpectedHeaders) {
      if (Phdr.p_type == llvm::ELF::PT_INTERP) {
        res = std::string(reinterpret_cast<const char *>(E.base() + Phdr.p_offset));
        break;
      }
    }
  }

  return res;
}

